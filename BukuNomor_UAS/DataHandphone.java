package BukuNomor_UAS;

public class DataHandphone {

    private int NO;
    private String Nomor;

    public DataHandphone(int NO, String Nomor) {
        this.NO = NO;
        this.Nomor = Nomor;
    }

    public int getNO() {
        return NO;
    }

    public String getNomor() {
        return Nomor;
    }
}
